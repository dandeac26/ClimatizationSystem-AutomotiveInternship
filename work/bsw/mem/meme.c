/*-------------------------------------------------------------------------------------------------
Function: g_bsw_mem_setTime



Description:
    Update the timeDate of extended_memRecorded with the reading (timeRead)



Parameters In:
     timeRead: time_t

Parameters Out:
     None



Return Value:
     None
-------------------------------------------------------------------------------------------------*/


void g_bsw_mem_setTime(time_t timeRead)
{

    extended_memRecorded.timeDate = timeRead;

}

/*-------------------------------------------------------------------------------------------------
Function: g_bsw_mem_getTime



Description:
     Updates timeRead with the value of extended_memRecorded.timeDate

Parameters In:
     None

Parameters Out:
     time_t* timeRead



Return Value:
     None
-------------------------------------------------------------------------------------------------*/

void g_bsw_mem_getTime(time_t* timeRead)
{

    *timeRead = extended_memRecorded.timeDate;

}

