/*KL15 - acc, engineRunning, sleep*/

int main(int argc, char **argv)
{
    system("cls");
    
    l_bsw_adc_readIgnition();

    checkSystem_state = ignition_status;

    while(1)
    {
        switch(checkSystem_state)
        {
            case STATE_KL0_SLEEP:
                
                state_helper(STATE_KL0_SLEEP);
                break;

            case STATE_KL15_WAKEUP:
                
                state_helper(STATE_KL15_WAKEUP);
                break;

            case STATE_KL30_RUN:
                
                state_helper(STATE_KL30_RUN);
                break;

            default:
                checkSystem_state = STATE_KL0_SLEEP;
                break;
        }
        
        menu();
        
    }
    
    return 0;
}